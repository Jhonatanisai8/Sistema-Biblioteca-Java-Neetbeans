    public void registror() {

        if (validarCampos()) {
            JOptionPane.showMessageDialog(null, "Por favor completar los campos faltantes", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else {
            String nombre = txtNombre.getText().trim();
            String apMaterno = txtApMaterno.getText();
            String apPaterno = txtApPaterno.getText();
            String domicilio = txtDomicilio.getText();
            String telefono = txtTelefono.getText();

            if (!validarTelefono(telefono)) {
                JOptionPane.showMessageDialog(null, """
                                                    Por favor el telefono
                                                    debe ser de 9 digitos""", "WARNING ", JOptionPane.WARNING_MESSAGE);
            } else {

                Usuarios usuario;

                if (isEdition) {
                    usuario = usuarioEditado;
                } else {
                    usuario = new Usuarios();
                }

                usuario.setNombre(nombre);
                usuario.setApellidoPaterno(apPaterno);
                usuario.setApellidoMaterno(apMaterno);
                usuario.setDomicilio(domicilio);
                usuario.setTelefono(telefono);

                try {
                    UsuarioDao usuarioDao = new DaoUserImplementacion();
                    if (!isEdition) {
                        usuarioDao.regristrar(usuario);
                    } else {
                        usuarioDao.modificar(usuario);
                    }
                    String mensaje;
                    if (isEdition) {
                        mensaje = "Usuario Modificado";
                    } else {
                        mensaje = "Usuario Registrodo";
                    }

                    JOptionPane.showMessageDialog(null, mensaje, "INSERTANDO DATOS", JOptionPane.INFORMATION_MESSAGE);
                    borrarCampos();
                } catch (HeadlessException e) {
                    String mensaje = "";
                    if (isEdition) {
                        mensaje = "modificar Usuario";
                    } else {
                        mensaje = "registrar Usuario";
                    }
                    JOptionPane.showMessageDialog(null, "Upss Algo paso al " + mensaje, "WARNING", JOptionPane.WARNING_MESSAGE);
                    System.out.println("Error en => " + e.toString());
                }
            }
        }

    }

