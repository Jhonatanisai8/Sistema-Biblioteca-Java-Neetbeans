    private void estilosPanel() {
        txtNombre.putClientProperty("JTextField.placeholderText", "Ingrese el nombre");
        txtApPaterno.putClientProperty("JTextField.placeholderText", "Ingrese el Apellido Paterno");
        txtApMaterno.putClientProperty("JTextField.placeholderText", "Ingrese el Apellido Materno");
        txtDomicilio.putClientProperty("JTextField.placeholderText", "Ingrese el Domicilio");
        txtTelefono.putClientProperty("JTextField.placeholderText", "Ingrese el Telefono");

        if (isEdition) {
            lblTitulo.setText("Informacion del Usuario");
            btnRegistrar.setText("Guardar");
            if (usuarioEditado != null) {
                txtNombre.setText(usuarioEditado.getNombre());
                txtApMaterno.setText(usuarioEditado.getApellidoMaterno());
                txtApPaterno.setText(usuarioEditado.getApellidoPaterno());
                txtDomicilio.setText(usuarioEditado.getDomicilio());
                txtTelefono.setText(usuarioEditado.getTelefono());
            }
        }
    }

    public boolean validarCampos() {
        return txtApMaterno.getText().trim().isEmpty()
                || txtApPaterno.getText().trim().isEmpty()
                || txtDomicilio.getText().trim().isEmpty()
                || txtNombre.getText().trim().isEmpty()
                || txtTelefono.getText().trim().isEmpty();
    }

    public boolean validarTelefono(String telefono) {
        return telefono.length() == 9;
    }

    private void borrarCampos() {
        txtApMaterno.setText(null);
        txtApPaterno.setText(null);
        txtDomicilio.setText(null);
        txtNombre.setText(null);
        txtTelefono.setText(null);
        txtNombre.requestFocus();
    }

